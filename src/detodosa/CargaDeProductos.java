    private void jbGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbGuardarActionPerformed
        // TODO add your handling code here:
        try{
        if(jtCodigo.getText().isEmpty()||jtDescripcion.getText().isEmpty()||jtPrecio.getText().isEmpty()||jtStock.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "Todos los capos deben estar llenos");
        }else{
            
        
        int codigo = Integer.parseInt(jtCodigo.getText());
        String descripcion = jtDescripcion.getText();
        double precio = Double.parseDouble(jtPrecio.getText());
        String rubro = (String)jcRubro.getSelectedItem();
        int stock = Integer.parseInt(jtStock.getText());
       
        
        
        MenuGeneral.listaProducto.add(new Producto(codigo,descripcion,precio,stock,Categoria.valueOf(rubro.toUpperCase())));
        limpiarCaja();
        }
        }catch(NumberFormatException e){
            JOptionPane.showMessageDialog(this, "Alguno o todos los camapos fueron mal ingresados");
            limpiarCaja();
        }
    }//GEN-LAST:event_jbGuardarActionPerformed

