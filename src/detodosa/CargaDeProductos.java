    private void jbEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbEliminarActionPerformed
        // TODO add your handling code here:
       try{ 
        int codigo = Integer.parseInt(jtCodigo.getText());
        Iterator<Producto> iterator = MenuGeneral.listaProducto.iterator();
        while (iterator.hasNext()) {
            Producto pr = iterator.next();
            if (codigo == pr.getCodigo()) {
                iterator.remove(); // Elimina el producto de la lista de manera segura
            
        
            }
              
        }
        limpiarCaja();
       }catch(NumberFormatException e){
           JOptionPane.showMessageDialog(this, "Debe ingresar un codigo");
       }
    }//GEN-LAST:event_jbEliminarActionPerformed


