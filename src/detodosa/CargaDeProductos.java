    private void jbBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbBuscarActionPerformed
            //Accion de buscar
            
            try{
                
                int cod= Integer.parseInt(jtCodigo.getText());
        for(Producto pr: MenuGeneral.listaProducto){
            
            if(cod== pr.getCodigo()){
            jtDescripcion.setText(pr.getDescripcion());
            jtPrecio.setText(String.valueOf(pr.getPrecio()));
            jtStock.setText(String.valueOf(pr.getStock()));
            String item = String.valueOf(pr.getRubro());
            if(item.equalsIgnoreCase("Comestible")){
               jcRubro.setSelectedIndex(0); 
            }
            if(item.equalsIgnoreCase("Limpieza")){
                jcRubro.setSelectedIndex(1);
            }
            if(item.equalsIgnoreCase("Perfumeria")){
                jcRubro.setSelectedIndex(2);
            }
            bloquearButton();
            
                
            }
        }
            }catch(NumberFormatException e){
                JOptionPane.showMessageDialog(this, "Para buscar debe tener un codigo y debe ser un entero");
                limpiarCaja();
            }
            
    }//GEN-LAST:event_jbBuscarActionPerformed

